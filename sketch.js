function setup() {
  createCanvas(400, 400);
  
  stroke(100,200,300);
  //changes outline color
  fill(300,200,100);
  //changes fill color
  strokeWeight(10);
  //makes border THICCC
  
  
  rectMode(CENTER);
  //centers at coord.
}

function draw() {
  background(220);
  //changes bg to monocrom color (can be hex)
  
  rect(200,200,200,200);
  //creates rectangle @ 50,50 coord.
  
  
}
